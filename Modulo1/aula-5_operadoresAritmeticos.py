""" Operadores aritmeticos """
soma=5+2
subtracao=5-2
multiplicacao=5*2
divisao=5/2
potencia=5**2
divinteira=5//2
restodiv=5%2
print('Resultado da soma, valor aritmetico +: ', soma)
print('Resultado da subtração, valor aritmetico -: ', subtracao)
print('Resultado da multiplicação, valor aritmetico *: ', multiplicacao)
print('Resultado da divisão, valor aritmetico /: ', divisao)
print('Resultado da potência, valor aritmetico **: ', potencia)
print('Resultado da divisão inteira, valor aritmetico //: ', divinteira)
print('Resultado da resto da divisão, valor aritmetico %: ', restodiv)